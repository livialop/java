public class RoletaInteiros {
    public static void main(String[] args) {

        int var1 = 2147483647;
        int var2 = 1000000000;

        System.out.println(var1 + var2);

        // O sistema começa uma espécie de roleta e depois do número máximo, ele reinicia pelo número mais alto negativo.
    }
}
